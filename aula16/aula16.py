"""
Manipulando Strings
# String indices
# Fatiamento de strings [ínicio:fim:passo]
# funções built-in len, abs, type, print, etc...
Cada função pode ser usada de uma maneira diferente em cada tipo
"""
nome = 'Python é Vida!'
print(nome[:6])
print(nome[7:8])
print(nome[9:15])      # Podem ser positivos
print(nome[-14:-8])    # Podem ser negativos

